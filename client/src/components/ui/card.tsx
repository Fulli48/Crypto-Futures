import * as React from "react"

import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    variant?: 'chart' | 'metric' | 'trading' | 'default'
  }
>(({ className, variant = 'default', ...props }, ref) => {
  const getVariantClass = () => {
    switch (variant) {
      case 'chart': return 'chart-card';
      case 'metric': return 'metric-card';
      case 'trading': return 'trading-card';
      default: return 'modern-card';
    }
  };

  return (
    <div
      ref={ref}
      className={cn(
        "border bg-gradient-to-br from-blue-900/30 via-blue-800/20 to-blue-900/30 text-blue-100 backdrop-blur-md border-blue-500/30",
        getVariantClass(),
        className
      )}
      {...props}
    />
  );
})
Card.displayName = "Card"

